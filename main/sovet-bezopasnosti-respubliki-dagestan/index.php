?><h2 style="text-align: center;"></h2>
<h2 style="text-align: center;"><span style="font-size: 13pt;">Государственные награды Республики Дагестан</span></h2>
<p style="text-align: justify;">
</p>
<hr>
 <br>
 <span style="font-size: 13pt;"> </span>
<p>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span><br>
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"><b>Государственные награды Республики Дагестан</b> являются формой поощрения граждан и признания их особых заслуг в развитии экономики, науки, культуры, просвещения, искусства, защите Отечества, воспитании, государственном строительстве, охране здоровья, жизни и прав граждан, укреплении мира и дружбы между народами, благотворительной деятельности и иных заслуг перед республикой. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span><br>
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> <b>Государственными наградами Республики Дагестан</b> являются:&nbsp;</span>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul style="text-align: justify;">
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">орден "За заслуги перед Республикой Дагестан", орден Почета Республики Дагестан,&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">орден Матери,&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">медаль "За вклад в социально-экономическое развитие Республики Дагестан",&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">медаль "За доблестный труд",&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">медаль "За заслуги в области физической культуры и спорта в Республике Дагестан",&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">медаль имени Амет-Хана Султана "За вклад в патриотическое воспитание молодежи",&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">Почетная Грамота Республики Дагестан,&nbsp;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 13pt;">почетный знак Республики Дагестан "За любовь к родной земле", почетные звания.</span>
	</p>
 </li>
</ul>
<p>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"><br>
 </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	При награждении <b>государственными наградами Республики Дагестан</b> вид награды определяется характером и степенью заслуг награждаемого. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Орден "За заслуги перед Республикой Дагестан" является высшей государственной наградой Республики Дагестан. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Орденом "За заслуги перед Республикой Дагестан" награждаются граждане за выдающиеся заслуги в развитии государственности Республики Дагестан, обеспечении прав и свобод граждан, развитии экономики, культуры, науки, образования, здравоохранения, искусства и спорта, за проявленные отвагу и мужество при охране общественного порядка. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Лицам, награжденным орденом "За заслуги перед Республикой Дагестан", выплачивается единовременное денежное поощрение в размере 50000 рублей за счет средств республиканского бюджета Республики Дагестан в порядке, определяемом Правительством Республики Дагестан. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Орденом Почета Республики Дагестан награждаются граждане за особые заслуги в различных отраслях экономики, науке, образовании, здравоохранении, культуре, спорте и в других областях трудовой деятельности. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Орден Почета Республики Дагестан имеет три степени: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<ul style="text-align: justify;">
	<li><span style="font-size: 13pt;">орден Почета Республики Дагестан I степени;</span></li>
	<li><span style="font-size: 13pt;">орден Почета Республики Дагестан II степени;</span></li>
	<li><span style="font-size: 13pt;">орден Почета Республики Дагестан III степени.</span></li>
</ul>
 <span style="font-size: 13pt;"> </span>
<p>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"><br>
 </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Высшей степенью ордена Почета Республики Дагестан является I степень. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Награждение орденом Почета Республики Дагестан производится последовательно, от низшей степени к высшей. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Лица, представляемые к ордену Почета Республики Дагестан III степени, как правило, должны быть награждены государственной наградой Республики Дагестан. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Орден Матери является государственной наградой Республики Дагестан, учреждаемой в целях повышения социальной роли и статуса женщины-матери, а также поощрения женщин в Республике Дагестан за заслуги в укреплении института семьи и воспитании детей. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Орденом Матери награждаются многодетные матери, постоянно проживающие на территории Республики Дагестан не менее 5 последних лет, родившие и (или) достойно воспитавшие десятерых и более детей, при достижении младшим ребенком возраста одного года. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Лицам, награжденным орденом Матери, выплачивается единовременное денежное поощрение в размере 50000 рублей за счет средств республиканского бюджета Республики Дагестан в порядке, определяемом Правительством Республики Дагестан. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Медалью "За вклад в социально-экономическое развитие Республики Дагестан" награждаются граждане за существенный вклад в социально-экономическое и культурное развитие Республики Дагестан, в развитие экономического и научного потенциала республики, за заслуги в государственной и общественной деятельности. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Медалью "За доблестный труд" награждаются граждане за высокие трудовые достижения в областях экономики, науки, культуры, просвещения, здравоохранения, в осуществлении мер по обеспечению законности, прав и свобод граждан, а также в других сферах трудовой деятельности. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Медалью "За заслуги в области физической культуры и спорта в Республике Дагестан" награждаются граждане за большие достижения в области физической культуры и спорта. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Медалью имени Амет-Хана Султана "За вклад в патриотическое воспитание молодежи" награждаются граждане и организации за значительный вклад в патриотическое воспитание молодежи. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Награждение Почетной Грамотой Республики Дагестан производится за особые заслуги в экономике, науке, культуре, искусстве, защите Отечества, государственном строительстве, воспитании, просвещении, охране здоровья, жизни и прав граждан, в укреплении мира и дружбы между народами, благотворительной деятельности и иные заслуги перед республикой. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Почетной Грамотой Республики Дагестан могут награждаться районы, города и другие населенные пункты, а также предприятия, учреждения и организации. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
