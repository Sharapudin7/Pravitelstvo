</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	Почетным знаком Республики Дагестан "За любовь к родной земле" награждаются граждане за значительный вклад в развитие сельского хозяйства и сельских территорий Республики Дагестан. </span>
</p>
