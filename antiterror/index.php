<p style="text-align: center;">
 <b><span style="font-size: 13pt;">Положение </span></b>
</p>
<p style="text-align: center;">
 <b><span style="font-size: 13pt;"> </span></b>
</p>
<p style="text-align: center;">
 <b><span style="font-size: 13pt;">
	об антитеррористической комиссии в субъекте Российской Федерации</span></b>
</p>
 <span style="font-size: 13pt;"> </span><br>
 <span style="font-size: 13pt;"> </span><br>
 <span style="font-size: 13pt;"> </span>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">1.&nbsp;Антитеррористическая комиссия в субъекте Российской Федерации (далее – Комиссия) является органом, образованным в целях координации деятельности территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации и органов местного самоуправления по профилактике терроризма, а также по минимизации и (или) ликвидации последствий его проявлений.&nbsp; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	2.&nbsp;Комиссия в своей деятельности руководствуется Конституцией Российской Федерации, федеральными конституционными законами, федеральными законами, указами и распоряжениями Президента Российской Федерации, постановлениями и распоряжениями Правительства Российской Федерации, законами и иными нормативными правовыми актами субъекта Российской Федерации, решениями Национального антитеррористического комитета, а также настоящим Положением. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	3.&nbsp;Руководителем Комиссии по должности является высшее должностное лицо (руководитель высшего исполнительного органа государственной власти) субъекта Российской Федерации (председатель Комиссии). </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	4. Основными задачами Комиссии являются: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	а)&nbsp;мониторинг политических, социально-экономических и иных процессов в субъекте Российской Федерации, оказывающих влияние на ситуацию в области противодействия терроризму, подготовка предложений Национальному антитеррористическому комитету по формированию государственной политики и совершенствованию нормативно-правового регулирования в области профилактики терроризма, минимизации </span><br>
 <span style="font-size: 13pt;">
	и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	б)&nbsp;координация деятельности территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации и органов местного самоуправления по профилактике терроризма, а также по минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	в)&nbsp;информационное сопровождение деятельности по профилактике терроризма в субъекте Российской Федерации, а также по минимизации и (или) ликвидации последствий его проявлений. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	5.&nbsp;Комиссия осуществляет следующие основные функции: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	а)&nbsp;в сфере мониторинга политических, социально-экономических </span><br>
 <span style="font-size: 13pt;">
	и иных процессов в субъекте Российской Федерации, оказывающих влияние на ситуацию в области противодействия терроризму, подготовки предложений Национальному антитеррористическому комитету по формированию государственной политики и совершенствованию нормативно-правового регулирования в области профилактики терроризма, минимизации и (или) ликвидации последствий его проявлений: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	анализ причин и условий возникновения и распространения терроризма на территории субъекта Российской Федерации, разработка мер по их устранению; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	мониторинг террористических угроз и террористической активности </span><br>
 <span style="font-size: 13pt;">
	в субъекте Российской Федерации, разработка мер по противодействию этим угрозам; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	подготовка предложений Национальному антитеррористическому комитету по совершенствованию нормативно-правового регулирования в области профилактики терроризма, минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	подготовка докладов в аппарат Национального антитеррористического комитета о результатах деятельности Комиссии, а также предложений </span><br>
 <span style="font-size: 13pt;">
	по совершенствованию деятельности в сфере профилактики терроризма, минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	б)&nbsp;в сфере координации деятельности территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации и органов местного самоуправления по профилактике терроризма, а также по минимизации и (или) ликвидации последствий его проявлений: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	анализ эффективности принимаемых территориальными органами федеральных органов исполнительной власти, органами исполнительной власти субъекта Российской Федерации и органами местного самоуправления мер по профилактике терроризма, а также минимизации </span><br>
 <span style="font-size: 13pt;">
	и (или) ликвидации последствий его проявлений, разработка предложений по их совершенствованию; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	разработка и координация исполнения мер по профилактике терроризма на территории субъекта Российской Федерации, в&nbsp;том числе по противодействию распространению идеологии терроризма и обеспечению антитеррористической защищенности потенциальных объектов террористических посягательств, мест массового пребывания людей; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	разработка и координация исполнения мер по минимизации и (или) ликвидации последствий проявлений терроризма на территории субъекта Российской Федерации; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	организация взаимодействия территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации и органов местного самоуправления </span><br>
 <span style="font-size: 13pt;">
	с общественными объединениями и религиозными организациями, другими институтами гражданского общества и гражданами, а также привлечение их к участию в противодействии терроризму; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	организация подготовки проектов и реализации государственных программ, планов и иных документов субъекта Российской Федерации по профилактике терроризма, а также минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	осуществление контроля за исполнением решений Комиссии; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	координация деятельности антитеррористических комиссий, образованных в муниципальных образованиях субъекта </span><br>
 <span style="font-size: 13pt;">
	Российской Федерации для организации взаимодействия органов местного самоуправления с подразделениями (представителями) территориальных органов федеральных органов исполнительной власти и органов исполнительной власти в субъекте Российской Федерации, по профилактике терроризма, минимизации и (или) ликвидации последствий его проявлений </span><br>
 <span style="font-size: 13pt;">
	(далее – антитеррористические комиссии в муниципальных образованиях); </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	методическое обеспечение и контроль деятельности антитеррористических комиссий в муниципальных образованиях; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	организация обучения сотрудников территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации и органов местного самоуправления, отвечающих за организацию в указанных органах мероприятий </span><br>
 <span style="font-size: 13pt;">
	по профилактике терроризма, а также по минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	подготовка предложений по обеспечению социальной защиты лиц, осуществляющих деятельность по борьбе с терроризмом и (или) привлекаемых к этой деятельности, а также лиц, пострадавших </span><br>
 <span style="font-size: 13pt;">
	от террористических актов; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	в)&nbsp;информационное сопровождение деятельности по профилактике терроризма в субъекте Российской Федерации, а также по минимизации и (или) ликвидации последствий его проявлений: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	своевременное информирование населения через средства массовой информации о мерах по профилактике терроризма, минимизации и (или) ликвидации последствий его проявлений, а также о принятии председателем Национального антитеррористического комитета или председателем Комиссии решений об установлении, изменении или отмене уровней террористической опасности, сроках, на которые они устанавливаются, </span><br>
 <span style="font-size: 13pt;">
	о границах участка территории субъекта Российской Федерации (об объекте), в пределах которого (на котором) они устанавливаются; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	обеспечение согласованности позиций территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации, иных государственных органов </span><br>
 <span style="font-size: 13pt;">
	и органов местного самоуправления при взаимодействии со средствами массовой информации по вопросам, касающимся освещения мер </span><br>
 <span style="font-size: 13pt;">
	по профилактике терроризма, минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	размещение в средствах массовой информации и информационно-телекоммуникационной сети «Интернет» материалов о деятельности Комиссии. </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	6. Комиссия для решения возложенных на нее задач имеет право: </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	а) принимать решения по вопросам, отнесенным к ее компетенции; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	б)&nbsp;запрашивать и получать в установленном порядке </span><br>
 <span style="font-size: 13pt;">
	от территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации и органов местного самоуправления, общественных объединений, организаций и должностных лиц необходимые материалы и информацию по вопросам, отнесенным к ее компетенции; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	в)&nbsp;создавать рабочие органы для изучения вопросов,</span><br>
 <span style="font-size: 13pt;">
	касающихся профилактики терроризма, минимизации и (или) ликвидации последствий его проявлений; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	г)&nbsp;привлекать для участия в работе Комиссии должностных лиц</span><br>
 <span style="font-size: 13pt;">
	и специалистов территориальных органов федеральных органов исполнительной власти, органов исполнительной власти субъекта Российской Федерации, иных государственных органов и органов местного самоуправления, а также представителей общественных объединений </span><br>
 <span style="font-size: 13pt;">
	и организаций (с их согласия); </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
	д)&nbsp;рекомендовать руководителям органов местного самоуправления субъекта Российской Федерации образование антитеррористических комиссий в муниципальных образованиях; </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;"> </span>
</p>
<p style="text-align: justify;">
 <span style="font-size: 13pt;">
