            <br><br>
            Среди древних предков народов Дагестана - племена легов, гелов, удинов и др., проживавшие на территории современного Дагестана в I тыс. до н.э. В конце I тыс. до н.э. территория Дагестана с указанными и другими племенами входила в состав Кавказской Албании. На рубеже двух эр Албания была вовлечена в тяжелые изнурительные войны римлян с парфянами, соперничающими между собой за гегемонию в Малой Азии и на Кавказе. В III в.н.э. южный Дагестан был захвачен Сасанидским Ираном, а в северный равнинный Дагестан в IV в. вторглись гунны.
        </p>
